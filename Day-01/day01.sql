# SQL - Day 1: Introduction to SQL

## 1. What is SQL?
SQL (Structured Query Language) is a standard language used to communicate with databases. It helps us create, store, retrieve, update, and delete data.

## 2. What is the need for SQL?
SQL is needed to manage data efficiently in a database. It allows us to insert, retrieve, update, and delete data.

## 3. Why do we learn SQL?
We learn SQL because it is an essential skill for managing databases and is widely used in jobs such as Data Analyst, MIS Executive, Software Developer, and Database Administrator.

## 4. Where is SQL used?
SQL is used in banks, hospitals, schools, e-commerce websites, social media platforms, and many IT companies.

## 5. How does SQL work?
SQL works by sending queries to a database. The database processes the query and returns the requested result.

## 6. Types of SQL Commands
- DDL (Data Definition Language)
- DML (Data Manipulation Language)
- DQL (Data Query Language)
- TCL (Transaction Control Language)
- DCL (Data Control Language)
